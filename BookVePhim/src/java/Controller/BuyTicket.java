    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        RoomDAO r = new RoomDAO();
        MovieDAO mv = new MovieDAO();
        String id = request.getParameter("id");
        int newId = Integer.parseInt(id);
        boolean check = mv.checkSaetMovie(newId);
        if (check == true) {
            request.setAttribute("done", "PHIM BẠN VỪA CHỌN ĐÃ HẾT VÉ");
            request.getRequestDispatcher("movieShowing").forward(request, response);
        } else {
            ArrayList<SeatRoom> rlist = r.getSeatStatusByMovieId(id);
            request.setAttribute("list", rlist);
            request.getRequestDispatcher("BuyTicket.jsp").forward(request, response);
        }
    }

